#include "libft.h"

int		ft_is_prime(int n)
{
	int		i;

	if ((n % 2 == 0) || (n < 0))
		return (0);
	i = 3;
	while (i < n / i)
	{
		if (n % i == 0)
			return (0);
		i += 2;
	}
	return (1);
}
