#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int i[2];
	int j;

	i[0] = 0;
	i[1] = 0;
	if (s2[0] == '\0')
		return ((char*)s1);
	while (s1[i[0]])
	{
		j = 0;
		if (s1[i[0]] == s2[j])
		{
			i[1] = i[0];
			while (s1[i[0]] && s1[i[0]] == s2[j] && s2[j])
			{
				j++;
				i[0]++;
			}
			if (s2[j] == '\0')
				return ((char *)&s1[i[1]]);
			i[0] = i[1] + 1;
		}
		i[0]++;
	}
	return (NULL);
}
